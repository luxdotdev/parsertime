function metricAggregateFilter(args: {
  id: string;
  metric?: string;
  label: string;
  table: string;
  column: string;
  aggregate: Aggregation;
  unit?: string;
}): FilterDef {
  return {
    id: args.id,
    label: args.label,
    table: args.table,
    column: args.column,
    source: "base",
    valueType: "number",
    operators: ["gte", "gt", "lte", "lt"],
    unit: args.unit,
    aggregate: args.aggregate,
    metric: args.metric,
  };
}

